# Ejemplo de tipos de datos en Python

# Entero
edad = 19
print('Edad:', edad)

# Numero con punto flotante
altura = 1.80
print('Altura:', altura)

# Cadena de texto
nombre = 'Vicente'
print('Nombre:', nombre)

# Tipo boolean
es_estudiante = False
print('Es estudiante?', es_estudiante)

# None, ausencia de valor
direccion = None
print('Direcci√≥n:',direccion)